package day1.datatypes;

public class DataType {

	public static void main(String[] args) {
		
		byte    a=100;
		int     i=12345678;
		char    c='D';
		float   f=0.15f;
		boolean b=false;
		
		String s="Kumar";
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(f);
		System.out.println(i);
		System.out.println(s);
		
	
	}

}
