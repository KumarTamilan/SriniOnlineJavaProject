package day15.selectionstatement;     //if-else-if-ladder

public class IfelseIf_Example {
	public static void main(String[] args) {
		int z =30;
		if(z==10) {
			System.out.println("This is if block");
		}
		else if(z==20) {
			System.out.println("This is else if block");
		}
		else if(z==30) {
			System.out.println("This is else if block");
		}
		else if(z==40) {
			System.out.println("This is else if ladder block");
		}
		else {
			System.out.println("This is else statement");
			}
	}
}
